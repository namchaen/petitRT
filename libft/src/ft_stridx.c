#include "libft.h"

int	ft_stridx(const char *s, int c)
{
	int	i;

	i = 0;
	while ((*(s + i) != (unsigned char)c))
	{
		if (*(s + i) == '\0')
			return (-1);
		i++;
	}
	return (i);
}
